# Num surto de modéstia, um@ alun@ resolveu brigar com outr@, se dizendo @ melhor e que el@ era @ pior... Para provar isso, juntou as notas de todos os alunos e mostrou as melhor e pior notas, assim como o valor da nota mediana. Apesar de ser tão competente, ele pediu sua ajuda para fazer um programa que resolva isso.

# Entrada
# A entrada consiste de uma linha contendo um número inteiro n, indicando quantas são as notas fornecidas (2≤n≤1000), seguida de n linhas, cada com um valor real indicando a nota de um aluno 0≤nota≤10.

# Saída
# Apresente, na primeira linha, o valor da pior nota, na segunda linha o valor da nota mediana, e na terceira linha o valor da melhor nota, todas com precisão de duas casas decimais.

# Observações
# É garantido que n é ímpar. A mediana é a nota central do conjunto ordenado.

