# O Decanato de Graduação da UnB fornece, quando possível, bolsas a alguns dos monitores das disciplinas. Quando não há bolsas para todos os alunos é preciso definir um critério para decidir quais serão monitores. Após muita discussão, o CIC decidiu utilizar o IRA como critério de ordenação de alunos, distribuindo as bolsas para os alunos de maior IRA.

# Para ajudar este processo, escreva um programa que leia uma quantidade de valores reais e os apresente em ordem decrescente.

# Entrada
# A entrada consiste de uma linha contendo um número inteiro n, indicando quantos são os alunos inscritos, seguida de n linhas, cada com um valor real indicando o IRA de cada aluno. O valor do IRA é tal que 0≤v≤5, e é garantido que 0<n≤200.

# Saída
# A saída consiste dos valores lidos, um por linha e com 2 casas decimais, em ordem decrescente.

