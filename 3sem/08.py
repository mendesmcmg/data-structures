# Na bela Tovarischlândia, a troca de recursos básicos é feita de maneira camarada, sem a noção de "valor agregado". O processo é simples, quando dois produtores se encontram com seus respectivos recursos, cada um divide seu conjunto em grupos com a máxima quantidade possível, e então cada um escolhe um desses grupos do outro para levar consigo. Assim, todos vivem felizes.

# Implemente um programa que, dadas as quantidade de cada recurso, defina a quantidade a ser trocada. A quantidade máxima deve ser calculada pelo máximo divisisor comum. 

# Entrada
# A entrada consiste de dois valores inteiros, separados por espaço, 1≤Q1,Q2≤10.000, indicando as quantidades.

# Saída
# Apresente, em uma linha, a quantidade de recursos a serem trocados.

# Observações
# No primeiro exemplo, o primeiro produtor teria 2 grupos de 4 recursos e o segundo produtor teria 3 grupos de 4 recursos.

# For example:

# Input	Result
# 8 12
# 4
# 5 15
# 5
# 123 2357
# 1
